#include "cub3d.h"

unsigned long	convert_rgb(int *rgb)
{
	return (((rgb[0] & 0xff) << 24) | ((rgb[1] & 0xff) << 16)
		| (rgb[2] & 0xff) << 8 | (255 & 0xff));
}
